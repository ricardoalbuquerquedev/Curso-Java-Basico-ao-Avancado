package voltandoAoComeco.Secao4;

public class RefazendoFor1 {
    public static void main(String[] args) {
        // for é a estrutura focada  em quantidade  determinada de repeticoes

        for(int contador=0; contador<=5;contador++){
            System.out.printf("i=%d\n", contador);
        }

        //A estrutura for tem 3 partes, 1- declaracao da variável, 2-expressao que retorna true or false
        // e a 3- manipulação da variavel de controle para que em determinado momento esse laco pare de exercutar
    }
}
