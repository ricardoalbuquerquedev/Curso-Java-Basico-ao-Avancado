package voltandoAoComeco.Secao3.operadoores;

public class Ternary {
    public static void main(String[] args) {

        double media = 5.9;
        String resultado = media >= 7.0 ? "Aprovado" : "Em Recuperação"; // 3 coisas ternario

        // na operacao ternário tem dois simbolos a ? q separa a minha expressao dos valores q sao atribuidos
        // e os : que separa se for falsa.
        System.out.println("O aluno está " + resultado );
    }
}
