package oo.revisao.Secao3.operadoores;

public class Unarios {
    public static void main(String[] args) {
        int a = 7;
        int d = 9;

        a++; //  a = a +1 incremento     // forma pos fixada a++

        a--; // a = a -1 decremento

        ++d; // d = d+1
        --d; // d = d-1

        System.out.println(a);
        System.out.println(d);

        System.out.println(a++ ==d--);


    }



}
